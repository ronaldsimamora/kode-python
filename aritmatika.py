def main():
    #mendefinisikan variabel x dan y
    x = 10
    y = 3
    
    #menampilkan nilai x dan y
    print("x = ", x)
    print("y = ", y)
    
    #melakukan perhitungan terhadap x dan y
    print("x + y = ", (x + y))
    print("x - y = ", (x - y))
    print("x * y = ", (x * y))
    print("x / y = ", (x / y))
    print("x % y = ", (x % y))
    print("x ** y = ", (x ** y))
    
if __name__ == "__main__":
    main()