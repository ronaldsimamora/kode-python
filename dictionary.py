def main():
    #membuat dan mengisi nilai ke dalam dictionary
    d = {'satu':10, 'dua':20, 'tiga':30}
    
    #menampilkan nilai dictionary
    print("d['satu]: ", d['satu'])
    print("d['dua']: ", d['dua'])
    print("d['tiga']: ", d['tiga'])
    print("d['dua'] * d['tiga']: ", d['dua'] * d['tiga'])
    
if __name__ == "__main__":
    main()
    